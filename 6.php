<?php
/*
 * Задача №6
 *
 * Функция возведения числа в степень.
 * $val - число, возводимое в степень
 * $pow - степень числа
 * Возвращает число $val, возведенное в степень &pow
 */
    function power($val, $pow)
    {
        if ($pow == 0)
            return 1;
        elseif ($pow < 0)
            return power(1 / $val, -$pow);
        else
            return $val * power($val, $pow - 1);
    }

    // Вывод "шапки"
    echo('<title>Задание 6</title>');
    echo('<h4>Задание 6:</h4>');

    echo('<hr>');
